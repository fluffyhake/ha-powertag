const char *ember_node_type_to_str(EmberNodeType node)
{
	switch (node) {
	case EMBER_NODE_UNKNOWN_DEVICE:
		return "unknown device";
	case EMBER_NODE_COORDINATOR:
		return "coordinator";
	case EMBER_NODE_ROUTER:
		return "router";
	case EMBER_NODE_END_DEVICE:
		return "end device";
	case EMBER_NODE_SLEEPY_END_DEVICE:
		return "sleepy end device";
	}
	return "unknown";
}
